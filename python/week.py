
from calendar import weekday


weekday = int(input("Enter Weekday Number.\n"))

if weekday == 1 :
    print("Sunday.\n")

elif weekday == 2 :
    print("Monday.\n")

elif weekday == 3 :
    print("Tuesday.\n")

elif weekday == 4 :
    print("Wednesday.\n")

elif weekday == 5 :
    print("Thursday.\n")

elif weekday == 6 :
    print("Friday.\n")

elif weekday == 7 :
    print("Saturday.\n")

else:
    print("Please enter weekday number between 1-7.\n")



